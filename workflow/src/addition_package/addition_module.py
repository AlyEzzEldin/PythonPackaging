class AddOperations:
    def __init__(self):
        pass
    def add_two_numbers(self, a, b):
        return a + b

    def increment_by_one(self, a):
        return a + 1

class SubtractOperations:
    def __init__(self):
        pass
    def subtract_two_numbers(self, a, b):
        return a - b

    def decrement_by_one(self, a):
        return a - 1